    private void inicializarVentanaYComponentes() throws SQLException, ClassNotFoundException {
        initComponents();
        this.setResizable(false);
        this.setLocationRelativeTo(null);
        this.setDefaultCloseOperation(JFrame.DO_NOTHING_ON_CLOSE);
        this.conexion = Conexion.getInstance().getConnection();
        listSolicitudes = cds.recuperarSolicitudes(conexion);

        btnSolicitudCambio.setVisible(false);
        
        if (usr.getId() == 12 || usr.getId() == 8) {
            if (listSolicitudes.isEmpty()) {
                btnSolicitudCambio.setVisible(false);
            } else {
                btnSolicitudCambio.setVisible(true);
            }
        }
    }

