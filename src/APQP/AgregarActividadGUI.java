    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int cantidadFamilias = apqps.obtenerFamilias(conexion);

        for (int i = 1; i < cantidadFamilias; i++) {
            ApqpM actividad = crearActividad(i);
            apqps.guardarActividad(conexion, actividad);
        }

        notificarActualizacion();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void inicializarVentanaYComponentes() {
        try {
            configurarVentana();
            inicializarAtributos();
        } catch (SQLException ex) {
            Utilidades.manejarExcepcion("Error al Abrir AgregarEtapaGUI: ", ex);
            Logger.getLogger(ApqpGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void configurarVentana() {
        initComponents();
        this.setResizable(false);
        this.setLocationRelativeTo(null);
        this.setDefaultCloseOperation(JFrame.DO_NOTHING_ON_CLOSE);
    }

    private void inicializarAtributos() throws SQLException {
        this.conexion = Conexion.getInstance().conectar();
        this.apqps = new ApqpServicio();
    }

    private ApqpM crearActividad(int indiceFamilia) {
        ApqpM actividad = new ApqpM();
        actividad.setEtapa(txtEtapa.getText());
        actividad.setFamilia("FAM. " + indiceFamilia);
        actividad.setActividad(txtNo.getText());
        actividad.setRequerimientos(txtPuntosYRequerimientos.getText());
        actividad.setFactibilidad(txtAnalisisFactibilidad.getText());
        actividad.setEquipo(txtEquipoGerencial.getText());
        return actividad;
    }

    private void notificarActualizacion() {
        cerrarVentana();
        System.out.println("pene");
        JOptionPane.showMessageDialog(this, "ARCHIVO ACTUALIZADO EXITOSAMENTE");
        apqps.abrirApqpGUI(usuario);
    }

    private void cerrarVentana() {
        AgregarActividadGUI.this.dispose();
        Conexion.getInstance().desconectar(conexion);
    }

